const os = require('os');

//Platform
console.log(os.platform());

// CPU architecture
console.log(os.arch());

// CPU core Infro
console.log(os.cpus());

// Free memory
console.log(os.freemem());

// Total memory
console.log(os.totalmem());

// home dir
console.log(os.homedir());

// Uptime
console.log(os.uptime());
